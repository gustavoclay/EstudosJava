package com.teste.cursojava.aula13;

public class OperadoresLogicos {

	public static void main(String[] args) {

		int num1 = 1;
		int num2 = 2;
		
		System.out.println("Valor1 == Valor2: " + (num1 == num2));
		System.out.println("Valor1 != Valor2: " + (num1 != num2));
		System.out.println("Valor1 > Valor2: " + (num1 > num2));
		System.out.println("Valor1 >= Valor2: " + (num1 >= num2));
		System.out.println("Valor1 < Valor2: " + (num1 < num2));
		System.out.println("Valor1 <= Valor2: " + (num1 <= num2));
		
	}

}
